package searchingAlgorithm;

public class BinarySearch {
    public int left;
    public int right;

    public BinarySearch(int left, int right) {
        this.left = left;
        this.right = right;
    }

    public void search(int left, int right) {

        while (left <= right) {
            int mid = (left + right) >> 1;
        }
    }
}
